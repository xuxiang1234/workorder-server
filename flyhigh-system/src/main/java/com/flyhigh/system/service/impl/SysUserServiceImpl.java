    /**
     * 获取管理员部门用户ID列表
     * @return
     */
    @Override
    public List<Long> getAdminUserIds() {
        String depId = configService.selectConfigByKey(SysConfigEnum.ROLE_ADMIN.getKey());
        if (StringUtils.isBlank(depId) || !StringUtils.isNumeric(depId)) {
            throw new ServiceException("管理员部门ID配置异常");
        }
        List<SysUser> sysUsers = this.selectUserListByDeptId(Long.valueOf(depId));
        return sysUsers.stream().map(SysUser::getUserId).collect(Collectors.toList());
    }

