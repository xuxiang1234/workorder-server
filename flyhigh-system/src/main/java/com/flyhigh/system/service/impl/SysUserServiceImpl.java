    /**
     * 通过userIds查询用户名称，超过两个人 拼接”等“字
     *
     * @param userIds
     * @return
     */
    @Override
    public String listUserNamesByIds(List<Long> userIds) {
        String userNames = StringUtil.EMPTY_STRING;
        if (!CollectionUtils.isEmpty(userIds)) {
            List<Long> subUserIds = ListUtil.sub(userIds, 0, 3);
            List<SysUser> users = userMapper.selectUserIds(subUserIds);
            userNames = users.stream().map(SysUser::getNickName).collect(Collectors.joining("、"));
            if (users.size() > 1) {
                userNames = userNames.concat("等");
            }
        }
        return userNames;
    }

